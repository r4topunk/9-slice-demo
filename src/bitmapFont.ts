export type BitmapTextOptions = {
  scale: number
  color: string
  letterSpacing?: number
  pixelSnap?: boolean
}

export const measureBitmapText = (text: string, scale: number, letterSpacing = 1): number => {
  const count = text.length
  if (count === 0) return 0
  const baseWidth = (GLYPH_WIDTH + letterSpacing) * count - letterSpacing
  return baseWidth * scale
}

export const bitmapTextHeight = (scale: number): number => GLYPH_HEIGHT * scale

export const drawBitmapText = (
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  options: BitmapTextOptions
): void => {
  const scale = options.scale
  const spacing = options.letterSpacing ?? 1
  const pixelSnap = options.pixelSnap ?? false
  const startX = pixelSnap ? snap(x) : x
  const startY = pixelSnap ? snap(y) : y

  ctx.save()
  ctx.fillStyle = options.color

  let cursorX = startX
  for (const rawChar of text) {
    const char = GLYPHS[rawChar] ? rawChar : rawChar.toUpperCase()
    const glyph = GLYPHS[char] ?? GLYPHS['?']
    for (let row = 0; row < GLYPH_HEIGHT; row += 1) {
      const line = glyph[row]
      for (let col = 0; col < GLYPH_WIDTH; col += 1) {
        if (line[col] === '1') {
          ctx.fillRect(
            cursorX + col * scale,
            startY + row * scale,
            scale,
            scale
          )
        }
      }
    }
    cursorX += (GLYPH_WIDTH + spacing) * scale
  }

  ctx.restore()
}
